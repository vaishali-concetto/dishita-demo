<script type="text/javascript">
$(document).on('click', '#btn_register', function (e){
    e.preventDefault();
    $("#register_form").validate({
        rules: {
            first_name : {
                required: true,
            },
            last_name : {
                required: true,
            },
            gender : {
                required: true,
            },
            phone: {
                required: true,
                number: true,
                minlength: 10,
                maxlength:10
            },
            email: {
                required: true,
                email: true
            },
            password: {
                required: true,
                minlength: 8
            },
            password_confirmation: {
                required: true,
                minlength: 8,
                equalTo: "#password"
            }
        },
        messages: {
            password_confirmation: {
                equalTo: "Please enter same as password."
            }
        }
    });

    if($("#register_form").valid()){
        $("#register_form").submit();
    }
})
</script>
